package run.soeasy.framework.core.exchange.event;

/**
 * 变更类型枚举，用于标识数据实体的变更操作类型。
 * <p>
 * 该枚举定义了三种基本的数据变更类型，广泛应用于事件通知、数据同步、状态审计等场景，
 * 为系统提供统一的变更操作语义描述。
 *
 * <p><b>核心特性：</b>
 * <ul>
 *   <li>标准化操作语义：明确区分创建、删除和更新三类基本变更操作</li>
 *   <li>事件驱动支持：为变更事件提供类型标识，便于事件处理和过滤</li>
 *   <li>可扩展性：设计上支持后续添加新的变更类型（如MERGE、RESTORE等）</li>
 * </ul>
 *
 * <p><b>典型应用场景：</b>
 * <ul>
 *   <li>数据库变更日志记录</li>
 *   <li>领域事件发布与订阅</li>
 *   <li>缓存失效策略控制</li>
 *   <li>数据版本控制与审计</li>
 * </ul>
 *
 * @author soeasy.run
 * @see ChangeEvent
 * @see BaseEvent
 */
public enum ChangeType {
    
    /**
     * 创建操作：表示新实体的创建或添加。
     * <p>
     * 典型场景：
     * <ul>
     *   <li>数据库插入新记录</li>
     *   <li>缓存中添加新条目</li>
     *   <li>领域模型创建新实体</li>
     * </ul>
     */
    CREATE,
    
    /**
     * 删除操作：表示现有实体的移除或删除。
     * <p>
     * 典型场景：
     * <ul>
     *   <li>数据库删除记录</li>
     *   <li>缓存中移除条目</li>
     *   <li>领域模型标记实体为删除状态</li>
     * </ul>
     */
    DELETE,
    
    /**
     * 更新操作：表示现有实体的修改或状态变更。
     * <p>
     * 典型场景：
     * <ul>
     *   <li>数据库更新记录字段</li>
     *   <li>缓存中更新条目值</li>
     *   <li>领域模型实体状态变更</li>
     * </ul>
     */
    UPDATE;
}